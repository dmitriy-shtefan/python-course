import turtle

turtle.shape("turtle")
turtle.color("red", "blue")
turtle.speed(1)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)

turtle.circle(20)

turtle.penup()

turtle.backward(100)

turtle.pendown()

turtle.circle(20)


turtle.done()